-- 두개(이상)의 테이블간에 Realation을 강제로 설정하여
-- 테이블에 추가되는 데이터의 무결성을 보증하는 제약조건 설정
-- JOIN을 사용하여 두개의 테이블에 Realation을 설정하기도 하는데
-- 그 차이점
-- JOIN은 단순히 View의 편리함(필요한 정보를 Projection 포함) 을 위해서 사용하기
-- FK는 데이터를 추가하는 단계부터 잘못된 데이터가 추가되는 것을 방지하는 목적
-- FK는 테이블을 생성할때 만들기도 하지만 
-- 일반적으로 테이블을 생성한 후 테이블 변경을 통해 만드는 것이 좋다.

-- 가장 좋은 경우는 테이블을 생성하고 데이터를 한개도 추가하지 않은 상태로
-- 설정하는 것이다.
-- 하지만 App을 사용하기 시작하는 초기단계에서는 다양의 데이터를 추가해야 하는
-- 일들이 많아서 이때 FK가 설정이 되어 있으면 SUB Table의 데이터를 추가하는데
-- 많은 애로사항이 발생한다.
-- 이런경우 초기에 FK를 설정하지 않거나, FK를 제거하고 데이터를 입력(추가)하도록
-- 하는 경우가 많다.
